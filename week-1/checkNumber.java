public class checkNumber {
    public static void main(String[] args) {
        int number = -5;

        if (number >= 3) {
            System.out.println("num >= 3");
        } else if (number == 3) {
            System.out.println("num = 3");
        } else {
            System.out.println("num < 3");
        }
    }
}
